# STREAM ALERTS LOG VALIDATION
# ─────────────────────────────────────────────────────────────────────────────
class TestStreamAlertsLog:

    @pytest.fixture(scope="class")
    def alerts_data(self):
        if not os.path.exists(ALERTS_LOG):
            pytest.skip(
                f"'{ALERTS_LOG}' not found. "
                "Run 'python src/stream_processor.py' first."
            )
        with open(ALERTS_LOG) as f:
            return json.load(f)

    def test_alerts_structure(self, alerts_data):
        """Alert log must contain required top-level keys."""
        required_keys = {"simulation_run", "events_processed", "anomalies_detected", "alerts"}
        assert required_keys.issubset(set(alerts_data.keys())), (
            f"Missing keys in alerts log: {required_keys - set(alerts_data.keys())}"
        )

    def test_events_processed_positive(self, alerts_data):
        """Simulation must have processed at least 1 event."""
        assert alerts_data["events_processed"] > 0, "No events were processed"

    def test_alert_records_have_required_fields(self, alerts_data):
        """Each alert must have the required fields for downstream consumers."""
        required_fields = {
            "alert_id", "severity", "listing_id",
            "borough", "reported_price", "neighbourhood_median"
        }
        for i, alert in enumerate(alerts_data.get("alerts", [])):
            missing = required_fields - set(alert.keys())
            assert not missing, (
                f"Alert #{i} is missing fields: {missing}"
            )

    def test_severity_values_valid(self, alerts_data):
        """Alert severity must be either WARNING or CRITICAL."""
        valid_severities = {"WARNING", "CRITICAL"}
        for alert in alerts_data.get("alerts", []):
            assert alert["severity"] in valid_severities, (
                f"Invalid severity '{alert['severity']}' — "
                f"must be one of {valid_severities}"
            )
